export interface TypeStatResult {
  icon: string;
  label: string;
  display: string;
}

export function calcTypeStat(overcomeCount: number, primaryType: UrgeType): TypeStatResult {
  const r = TYPE_REWARDS[primaryType];
  const total = overcomeCount * r.perOvercome;
  let display = "";
  if (r.format === "money") {
    display = `約${total >= 10000 ? `${Math.floor(total / 1000)}k` : total.toLocaleString()}円`;
  } else if (r.format === "cal") {
    display = `約${total.toLocaleString()}kcal`;
  } else if (r.format === "min") {
    display = total >= 60
      ? `約${Math.floor(total / 60)}時間${total % 60 ? (total % 60) + "分" : ""}`
      : `約${total}分`;
  } else {
    display = `${total}回`;
  }
  return { icon: r.icon, label: r.label, display };
}
